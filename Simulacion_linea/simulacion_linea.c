extern char g_pulsada;

extern int linea;

/*********************************************************************
** 																	**
** LOCAL FUNCTIONS 													**
** 																	**
**********************************************************************/

void cantidad_linea(){

	switch(g_pulsada){
	case RIGHT: linea=1;
				break;
	case LEFT: 	linea=2;
				break;
	case DOWN:	linea=3;
				break;
	}

}

